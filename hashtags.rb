puts "Введите строку с хэштегами:"
answer = STDIN.gets.chomp

# [[:word:]] - неограниченное кол-во букв до пробела
# - - нижнее подчеркивание и дефис/тире
# | - логическое или
hash = answer.scan(/#[[:word:]-]+|#/)

# .join - преобразовывает каждый элемент массива в строку,
# разделенную  аргументом в скобках (если аргумент отсутствует -
# выведет слитно)
puts hash.join(', ')